                halten und &auml;u&szlig;ern Sie eventuelles Missgefallen erst nach den Weihnachtsfeiertagen. Es w&uuml;rde sonst nur zu
                unangenehmen Situationen f&uuml;hren und w&uuml;rde die friedliche Stimmung zerst&ouml;ren.
