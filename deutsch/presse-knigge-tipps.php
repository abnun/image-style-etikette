			<b>Knigge-Tipp M&auml;rz</b>

            <p>
				Dieses Mal habe ich mich f&uuml;r ein Detail entschieden, das jeden betrifft: <b>die Serviette!</b>
				<br />
				Sie wird w&uuml;rdevoll vom Servicepersonal in die verschiedensten Formen gefaltet, sie schm&uuml;ckt den
				gedeckten Tisch und sch&uuml;tzt uns hin und wieder vor Beweisen der Men&uuml;folge auf unserer Kleidung.
            </p>

            <p>
				Die erste Frage stellt sich schon gleich nach dem Platznehmen am Tisch. Dieses gefaltete Kunstwerk steht
				uns beim Studieren der Speisekarte enorm im Weg.
				Nicht z&ouml;gern, wir d&uuml;rfen die Serviette n&auml;mlich aus dem Weg r&auml;umen, besten falls rechts neben uns auf dem
				Tisch ablegen oder einfach nur vor uns umlegen.
				Sobald wir bestellt haben, gibt es nur einen einzigen Platz f&uuml;r die Serviette, und das ist unser Scho&szlig;.
				Somit die zweite Frage: wie lege ich die Serviette auf den Scho&szlig;? Auch hier gibt es die optimale L&ouml;sung:
				die Serviette "aufbrechen", wie es im Fachjargon hei&szlig;t, und einmal gefaltet mit der offenen Seite zum
				Bauch ablegen.
            </p>

            <p>
				W&auml;hrend dem Essen ist die Serviette vor jedem Griff zum Glas zu benutzen, damit die Men&uuml;folge nicht an
				unserem Glasrand abzulesen ist.
            </p>

            <p>
				M&uuml;ssen wir den Tisch verlassen, um die Erfrischungsr&auml;ume aufzusuchen, legen wir die Serviette rechts
				neben dem Gedeck ab. Nach der R&uuml;ckkehr findet sie wieder den Platz auf unserem Scho&szlig;.
            </p>

            <p>
				Haben wir das Mahl beendet, handeln wir grob fahrl&auml;ssig, wenn wir die Stoffserviette nun in den
				beschmutzen Teller legen und werden von der Knigge-Polizei unverz&uuml;glich verhaftet. Die Serviette findet
				ihren Platz links neben uns auf dem Tisch. Bei der Papierserviette m&uuml;ssten wir beide Augen zudr&uuml;cken,
				w&uuml;rde diese im Teller landen.
            </p>

			<p>
				Meinen aufrichtigen Dank an den Erfinder der Serviette!
