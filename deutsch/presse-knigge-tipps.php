			<b>Der Weihnachts-Knigge</b>
			<br/>
			<p>
                Wie sage ich, dass mir das Geschenk nicht gef&auml;llt, und wann darf ich los zur Party?
            </p>
			<p>
				Knigge-Coach Birgit Brenner verr&auml;t, wie es ein friedliches Fest wird und welche Faux-Pas Sie wie vermeiden.
            </p>

            <br />
            <b>
				Welche Kleidung ist zum Fest angemessen?
            </b>

            <p>
				Weihnachten ist ein gediegenes Fest, das sollte man auch in der Kleidung sehen. F&uuml;r Frauen gilt: keine
				zu tiefen Ausschnitte oder zu kurze R&ouml;cke. Anzug muss es nicht unbedingt sein, Jogginghose geht aber gar
				nicht.
            </p>

			<br />
            <b>
				Was mache ich, wenn mir der Festtagsbraten der Gastgeberin nicht schmeckt?
            </b>

            <p>
				Bleiben Sie diplomatisch. Sagen Sie etwa: "ich habe heute nicht viel Appetit" oder essen Sie daf&uuml;r mehr
				von den Beilagen.
            </p>

			<br />
            <b>
				Ein Geschenk gef&auml;llt mir nicht. Wie reagiere ich?
            </b>

            <p>
				Oberste Regel: Erw&auml;hnen Sie das nicht an diesem Abend! Rufen Sie einige Tage sp&auml;ter noch mal an,
				bedanken Sie sich f&uuml;r die Einladung und sagen Sie dann vielleicht: "Genau das Geschenk habe ich schon".
				Oder "Die Farbe steht mir nicht. W&auml;re es ein Problem, das umzutauschen?"
            </p>

