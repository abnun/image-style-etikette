			<b>Knigge-Tipp April</b>

            <p>
				Oster-Knigge
            </p>

            <p>
			  Ich habe mir lange &uuml;berlegt, ob es einen speziellen Oster-Knigge gibt.
			  <br />
			  Nun, f&uuml;r viele ist es eben ein schon lange ersehntes verl&auml;ngertes Wochenende, das freiwillig oder eher gezwungenerma&szlig;en,
			  h&auml;ufig auch mit einem Besuch bei der Familie verbunden wird. Manche haben sich mit einer Reise in den S&uuml;den geschickter
			  Weise aus dem Staub gemacht. F&uuml;r die Daheimgebliebenen hier nun einige Tipps:
            </p>

            <p>
			   Ostern soll ein geselliges Familienfest sein. Das Fest verlangt nach einer friedlichen Stimmung,
			   einfachen und konfliktfreien Gespr&auml;chsthemen und nach einem Wochenende, an das man gerne zur&uuml;ckdenkt.
			   Wir sind nicht gezwungen, vier Tage am Tisch zu sitzen und schon nach dem ersten Tag zwanghaft nach "neutralen"
			   Gespr&auml;chsthemen zu gr&uuml;beln und die einzige Abwechslung der Gastgeber ist.
			   <br />
			   Unternehmen Sie mit Ihren Lieben etwas! Abwechslung ist hier gefragt und entzerrt geschickt lange Gespr&auml;chs-Durststrecken.
            </p>

            <p>
				Die Osterfeiertage sind auch die Er&ouml;ffnung des Fr&uuml;hlings. Die Natur hat es bemerkt, bl&uuml;ht und gr&uuml;nt im
				neuen bunten Kleid! Wir sollten die w&auml;rmere Jahreszeit ebenfalls nutzen und drau&szlig;en etwas unternehmen.
            </p>

            <p>
			  	"Eier suchen" ist auch in heutiger Zeit noch sehr aktuell bei Kindern.
